#include <stdio.h>

/**
 * add - adds two integers
 * @a: first int
 * @b: second int
 * Return: addition
 */

int add(int a, int b)

{
	return (a + b);
}

/**
 * sub - subtract two integers
 * @a: first int
 * @b: second int
 * Return: subtracted result
 */

int sub(int a, int b)

{
	return (a - b);
}

/**
 * div - divide two integers
 * @a: first int
 * @b: second int
 * Return: division number
 */

int div(int a, int b)

{
	return (a / b);
}

/**
 * mod - mod of two integers
 * @a: first int
 * @b: second int
 * Return: the final result
 */

int mod(int a, int b)

{
	return (a % b);
}

/**
 * mul - multiply two integers
 * @a: first int
 * @b: second int
 * Return: multiplied result
 */

int mul(int a, int b)

{
	return (a * b);
}
